from __future__ import annotations

from dataclasses import dataclass


@dataclass(frozen=True)
class PersonalityProfile:
    code: str
    name: str
    title: str
    themeColor: str
    emoji: str
    rarity: str
    coreDescription: str
    funCopy: str
    portrait6d: dict[str, int]
    tags: list[str]

    def to_dict(self) -> dict:
        return {
            "代号": self.code,
            "名称": self.name,
            "称号": self.title,
            "主题色": self.themeColor,
            "emoji": self.emoji,
            "稀有度": self.rarity,
            "核心描述": self.coreDescription,
            "趣味文案": self.funCopy,
            "画像6维": dict(self.portrait6d),
            "标签": list(self.tags),
        }


def list_all_personalities() -> tuple[PersonalityProfile, ...]:
    return _ALL_PERSONALITIES


def get_personality_by_code(code: str) -> PersonalityProfile:
    normalized = (code or "").strip().upper()
    if not normalized:
        raise ValueError("人格代号不能为空")
    profile = _PERSONALITY_BY_CODE.get(normalized)
    if profile is None:
        raise ValueError(f"未知人格代号：{normalized}")
    return profile


def _portrait(
    loudness: int,
    pitch: int,
    endurance: int,
    chaos: int,
    stability: int,
    burst: int,
) -> dict[str, int]:
    return {
        "响度": int(loudness),
        "音调": int(pitch),
        "持久": int(endurance),
        "混沌": int(chaos),
        "稳定": int(stability),
        "爆发": int(burst),
    }


